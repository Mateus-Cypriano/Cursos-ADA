// OPERADORES ARITIMETICOS

let numero1 = 20;
let numero2 = 2;

// Adição:          +
// Subtração:       -
// Multiplicação    *
// Divisão:         /
// Exponenciação:   **
// Resto da divisão: %

console.log('Soma = ' , numero1 + numero2);
console.log('Subtração = ' , numero1 - numero2);
console.log('Multiplicação = ' , numero1 * numero2);
console.log('Divisão = ' , numero1 / numero2);
console.log('Exponenciação = ' , numero1 ** numero2);
console.log('Resto da divisão = ' , numero1 % numero2);

// precedencia de operadores

console.log(3 + 7 * 3); //primeiro ele sempre calcula a multplicação - assim como na matematica.

console.log((3 + 7) * 3); // para calcular a soma primeiro, coloque o parentese.

console.log((10 + 7 + 8) / 3); 
